package day03_VariablesContinue;

public class Circle {

    public static void main(String[] args) {

        double pi = 3.14;
        double radius = 4.5;
        double area = pi *(radius * radius);
        double parameter = pi *(2*(radius * radius));
        System.out.println(area);
        System.out.println(parameter);
    }
}
